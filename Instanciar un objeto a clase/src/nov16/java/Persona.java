/*

la clase Persona tiene un atributo privado edad y dos métodos públicos getEdad() y setEdad() para obtener y establecer el valor de ese atributo, respectivamente.
*/

package nov16.java;

public class Persona 
{
    //Atributos
    private int edad;
    
    //Metodos
    public int getEdad()
    {
        return this.edad;
    }
    public void setEdad(int x)
    {
        this.edad= x;
    }
}
